uint8_t input_pos = 0;
uint8_t enc_key = 0;

extern const unsigned char * song_Data[];

void
vblank()
{
    f++;
    text_pos-=1;

    // input
    currkey = joypad();
    if ((currkey & J_A) && !(prevkey & J_A)) {
        enc_key |= (1 << input_pos);
        input_pos++;
        key=1;
    } else if ((currkey & J_B) && !(prevkey & J_B)) {
        enc_key &= ~(1 << input_pos);
        input_pos++;
        key=1;
    } else {
        key=0;
    }
    prevkey = currkey;

    if (key) {
        for (i = 0; i < text_len; i++) {
            set_sprite_tile(i, (flag[i] ^ (enc_key+i)) % 93);
        }
        if (input_pos >= 8) {
            input_pos = 0;
            enc_key = 0;
        }
    }

    gbt_update();
}

void main() {
    disable_interrupts();
    gbt_play(song_Data, 2, 7);
    gbt_loop(1);

    set_interrupts(VBL_IFLAG);
    enable_interrupts();

    set_bkg_data(0, bg_tile_count, bg_tile_data);
    VBK_REG = 1;
    VBK_REG = 0;
    set_bkg_tiles(0, 1, 30, 18, bg_map_data);
    SHOW_BKG;
    DISPLAY_ON;

    // Init sprites
    SPRITES_8x8;
    set_sprite_data(0, 94, font_tile_data);
    for (i = 0; i < text_len; i++) {
        set_sprite_tile(i, initial_text[i]);
    }

    SHOW_SPRITES;
    add_VBL(vblank);
    while (1) {
	    if (LY_REG >= 0 && LY_REG <= 134) {
            SCX_REG = sin[(LY_REG + f) & 0xFF];
	    } else {
	        for (i = 0; i < text_len; i++)
                move_sprite(i, 140, text_pos+i*8);
	    }
    }
}
